import cv2

rasp = cv2.imread("entrada.jpg")
listaVerde = []
listaVermelho = []
resultadoVerde = []
resultadoVermelho = []

########################## Lista Verde ##########################

for i in range(0, rasp.shape[0]):
    for j in range(0, rasp.shape[1]):
        listaVerde.append(rasp[i][j][1])

listaSemVerde = sorted(set(listaVerde))

for i in range(0, len[listaSemVerde]):
    somatoria = 0
    for j in range(0, len[listaVerde]):
        if listaSemVerde[i] == listaVerde[j]:
            somatoria += 1
    resultadoVerde. append(somatoria)

print(resultadoVerde)

